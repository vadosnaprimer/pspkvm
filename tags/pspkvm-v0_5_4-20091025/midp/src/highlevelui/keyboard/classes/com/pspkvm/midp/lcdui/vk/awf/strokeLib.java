    public char [] getHzByStrokeString(String stroke){
		System.out.println("getHzByStrokeString="+stroke);
		if(stroke!=null){
			int pos=0,i=0;
			while(i<stroke.length()){
				char ch=stroke.charAt(i);
				if(ch<'0'||ch>'5') return null;
				if(pos<=1000*6){
					pos=strokeTree[pos+(ch-'0')]*6;	
				}else if(pos<=2000*6){
					pos-=1000*6;
					pos=slib1.getStrokLib()[pos+(ch-'0')]*6;
				}else{
					pos-=2000*6;
					pos=slib2.getStrokLib()[pos+(ch-'0')]*6;
				}	
				i++;
				System.out.println("getHzByStrokeString pos="+pos+"char="+ch);
			};
			if(pos<=1000*6){
				pos=strokeTree[pos];	
			}else if(pos<=2000*6){
				pos-=1000*6;
				pos=slib1.getStrokLib()[pos];
			}else{
				pos-=2000*6;
				pos=slib2.getStrokLib()[pos];
			}
			System.out.println("getHzByStrokeString TruePos="+pos);
			if(pos==0)return null;
			return Hz.getStrokeNode(pos/2);
		};
		return null;
	}

